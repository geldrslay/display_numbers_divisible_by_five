# Display numbers divisible by 5 from a list
# Iterate the given list of numbers and print only those numbers which are divisible by 5

# Display given list. 
number_list1 = [10, 20, 33, 46, 55]
print ('The given list is:', number_list1)

# Determine and display numbers divisible by five from the list
print ('The numbers divisible by 5 are:')
for numbers in number_list1:
    if numbers % 5 == 0:
        print (numbers)

